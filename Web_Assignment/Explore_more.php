                <h2>Measures of dispersion for Ungrouped Data</h2>
                <p>
                    Measures of dispersion for ungrouped data are statistical tools used to quantify the spread or variability of 
                    individual data points in a dataset. Unlike measures of central tendency that focus on the typical or central 
                    value of a dataset, measures of dispersion provide insight into how much the individual data points deviate 
                    from the central value. Common measures of dispersion for ungrouped data include the range, variance, and 
                    standard deviation. The range simply calculates the difference between the maximum and minimum values in the 
                    dataset, offering a basic understanding of the spread. Variance, on the other hand, computes the average 
                    squared deviation of each data point from the mean, providing a more comprehensive measure of variability. 
                    Standard deviation, the square root of the variance, is often preferred as it represents dispersion in the 
                    same units as the original data. These measures are invaluable in fields such as finance, psychology, and 
                    biology, where understanding the variability of data is crucial for making informed decisions, assessing 
                    risk, and drawing meaningful conclusions from empirical observations. They allow researchers and analysts to 
                    grasp the extent to which individual data points deviate from the central tendency, providing essential 
                    context for interpreting the data and drawing meaningful insights.
