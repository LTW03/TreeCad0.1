                <p class="underline">
                    <strong>Setting goals</strong>
                </p>
                <ul>
                    <li>It is a first step in the financial management process.</li>
                    <li>The financial goals must be prioritised and specific.</li>
                </ul>
                <p>
                    <Strong>Short-term financial goals</Strong>
                </p>
                <ul>
                    <li>It can be achieved in less than a year. Short term financial goals do not involve a
                        <br>
                        large amount of money for example: purchasing a laptop, furniture, a cell phone and others.
                    </li>
                </ul>
                <p>
                    <strong>Long-term financial goals</strong>
                    <br>
                    It usually takes more than five years to achieve.
                    <br>
                    Long term financial goals involve a large amount for example:children's
                    <br>
                    education, medical expenses and others.
                </p>
                <p>
                    <strong>SMART financial goals</strong>
                </p>
                <p>
                    Financial goals set based  on the SMART concept our spending in order to achieve the
                    <br>
                    desired financial goals :
                    <ul>
                        <li>S - Specific</li>
                        <li>M - Measurable</li>
                        <li>A - Affordable</li>
                        <li>R - Reliable</li>
                        <li>T - Time_bound</li>
                    </ul>
                </p>

                <p class="text">
                    <strong>2. Evaluating financial status</strong>
                </p>
                <ul>
                    <li>Assets and liabilities are the benchmarks for evaluating our financial status.</li>
                    <li>Evaluating our financial status helps us measure our performance in the effort of achieving our short-term and long-term financial goals.</li>
                </ul>
                <p class="underline">
                    <strong>Creating financial plan</strong>
                </p>
                <p>
                    <strong>Steps to consider before creating a financial plan :</strong>
                    <ul>
                        <li>Define the term and long term goals</li>
                        <li>Make an initial budget to achieve each goal</li>
                        <li>Calculate monthly savings needed to achieve the short-term and long-term goals.</li>
                        <li>Analyse spending behavior</li>
                        <li>Set a time frame to achieve each goal </li>
                        <li>Determine income strategies that will help to achieve the financial goals.</li>
                    </ul>
                </p>
                <p class="underline">
                    <strong>4. Carrying out financial plan</strong>
                </p>
                <ul>
                    <li>It turns the financial planning into action that can be implemented.</li>
                    <li>When carrying out a financial plan, we must follow the plan at an early stage.</li>
                    <li>We must ready to change and compare the planned monthly expenses and actual expenses.</li>
                </ul>
                <p class="underline">
                    <strong>5. Reviewing and revising the progress</strong>
                </p>
                <ul>
                    <li>Reviewing and revising the progress of a financial plan from time to time is important to make sure the cash flow is always positive.</li>
                    <li>This indirectly helps us to achieve our financial goals as planned.</li>
                </ul>
                <p>
                    <strong>Evaluation the feasibility of the short-term goals and long-term financial goals</strong>
                </p>
                <ul>
                    <li>An effective financial plan should be get aside 10% savings of the total income prior to engaging any fixed and variable expenses.</li>
                    <li>If there is a negative cash flow, we should adjust the financial plan by reducing the variable expenses.</li>
                </ul>
